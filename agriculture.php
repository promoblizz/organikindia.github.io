											 <h3><span class="text-theme-color">Solution</span></h3>
                                                <p align="justify">Hard water from a bore (well) typically has very high surface tension. This water is difficult to be taken up into the plant stem and absorbed into the cell structure. and If it is hard water then it becomes more difficult to be taken up by plant. It is not well absorbed and the result is the plant growth becomes slower. On the other hand any hard water that is treated with Electrical induction'Puroo Water softener' the surface tension of the water is lowered significantly. This then allows the water and minerals to be easily drawn up by the plant. The water provides support and the minerals provide nourishment so that the plant growth will faster and total yield will increase.</p>
                                                <p align="justify">Puroo Water softener changes the chemical structure of the hard Water. This forces to decomposes the molecules into a new form, It just changes molecular structure of the minerals which makes it in pure form , so the calcium, iron and other minerals stay in the water but never form hard scale at walls of piping, fixtures, and appliances. These all silent minerals are very much important for crop and human. The water coming from Puroo water softener never form a crusty layer of salt or iron on the ground , these all minerals are more easily absorbed into the ground and the crops, which gives both a healthier environment to grow crops and increases the yield.</p>
