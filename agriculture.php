											 <h3><span class="text-theme-color">Agriculture Applications</span></h3>
                                                <p align="justify">Water is the life blood of any farm. In nature water never exists as just pure H2O. To make available best water from canal or river for farming is difficult. Almost 85% farmers have boar well or well water for farming. There are always other substances dissolved in it that we know as mineral salts.  Healthy, balanced water needs to have mineral salts in it to sustain life. However it's the percentages of those mineral salts that make water either "hard" or "soft". Basically, high amounts of minerals, such as calcium and magnesium, classify water as hard.  You can often find a large amount of iron, manganese, sodium and other dissolved compounds such as bicarbonates and sulphates in hard water.</p>
												 <h4><span class="text-theme-color">Hard Water Problems</span></h4>
                                                <p align="justify">Due to large amount of minerals present in water , water becomes hard , when it comes in water pipe line, drip irrigation line and sprinkler nozzle get choked over a period of time and there is no uniform water supply to all crops. Also hard TDS water burns tips of leaves, reduces growth of the plant, affects crops yield as compared to normal water farming and also damages the soil structure.</p>
